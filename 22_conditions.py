number = 10

#     True
if number == 10:
    print("number = 10")


#      False
if number != 10:
    print("number != 10")

#      False
if number < 10:
    print("number < 10")


#     False
if number > 10:
    print("number > 10")


#     True
if number <= 10:
    print("number <= 10")

#     True
if number >= 10:
    print("number >= 10")